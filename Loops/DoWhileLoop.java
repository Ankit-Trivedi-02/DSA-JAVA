/* A basic explanation of do while for loop */
package Loops;
public class DoWhileLoop {
    public static void main(String[] args) {
        /*
         * do{
         * your code
         * }while(condition);
         */
        int i = 1;
        do {
            System.out.println(i);
            i++;
        } while (i < 11);
    }
}
